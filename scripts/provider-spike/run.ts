import {
  BENGALI_TEXT_PROMPT,
  PROVIDER_SPIKE_PROMPT_VERSIONS,
  STRUCTURED_PROMPT,
  THINKING_COMPARISON_PROMPT,
  TRANSCRIPTION_PROMPT,
} from "../../src/infrastructure/gemma/provider-spike-prompts.js";
import {
  PROVIDER_SPIKE_SCHEMA_VERSION,
  providerSpikeStructuredJsonSchema,
  providerSpikeStructuredSchema,
  type ProviderSpikeStructuredValue,
} from "../../src/infrastructure/gemma/provider-spike-schemas.js";
